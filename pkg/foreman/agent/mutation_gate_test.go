func TestIsGeneratedGoFile(t *testing.T) {
	ws := t.TempDir()
	write := func(rel, content string) {
		full := filepath.Join(ws, rel)
		_ = os.MkdirAll(filepath.Dir(full), 0o755)
		if err := os.WriteFile(full, []byte(content), 0o644); err != nil {
			t.Fatal(err)
		}
	}
	write("api/zz_generated.deepcopy.go", "package v1\n")
	write("pkg/x/thing.pb.go", "package x\n")
	write("pkg/x/gen.go", "// Code generated by foo. DO NOT EDIT.\npackage x\n")
	write("pkg/x/hand.go", "package x\n\nfunc F() {}\n")

	cases := map[string]bool{
		"api/zz_generated.deepcopy.go": true,
		"pkg/x/thing.pb.go":            true,
		"pkg/x/gen.go":                 true,
		"pkg/x/hand.go":                false,
	}
	for path, want := range cases {
		if got := isGeneratedGoFile(ws, path); got != want {
			t.Errorf("isGeneratedGoFile(%q)=%v want %v", path, got, want)
		}
	}
}

